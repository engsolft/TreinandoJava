package basico;

public class OpMat4 {

	public static void main(String[] args) {
		int resultado1 = 10+3;
		int resultado2 = 10-3;
		int resultado3 = 10*3;
		int resultado4 = 10/3;
		
		System.out.println("10 + 3 = " + resultado1);
		System.out.println("10 - 3 = " + resultado2);
		System.out.println("10 * 3 = " + resultado3);
		System.out.println("10 / 3 = " + resultado4);

	}

}
